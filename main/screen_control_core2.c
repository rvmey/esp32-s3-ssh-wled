/*
 * screen_control_core2.c
 *
 * ILI9342C SPI display driver for the M5Stack Core2 for AWS (320 x 240).
 * Implements the screen_control.h API so picture_frame.c can be shared
 * between the JC3248W535 and Core2 variants without modification.
 *
 * Hardware wiring (M5Stack Core2)
 * ─────────────────────────────────
 *   ILI9342C SPI     MOSI=GPIO23  MISO=GPIO38  SCK=GPIO18  CS=GPIO5  DC=GPIO15
 *   AXP192 PMU       I2C addr 0x34 on SDA=GPIO21 / SCL=GPIO22
 *   FT6336U touch    I2C addr 0x38 on the same bus
 *
 * Orientation
 * ───────────
 *   Physical panel is 320×240 (landscape native).
 *   s_landscape=true  → landscape 320×240 (default for Core2)
 *   s_landscape=false → portrait  240×320 (software-rotated 90° CW)
 *
 * NOTE: MADCTL colour-order byte may need tuning (0x48 vs 0x08 etc.)
 * if the image appears mirrored or has swapped red/blue channels on
 * a specific panel batch.  See MADCTL_LANDSCAPE below.
 */

