esp_err_t screen_init(void)
{
    s_draw_mutex = xSemaphoreCreateMutex();

    /* ── I2C (AXP192 + FT6336U) ─────────────────────────────────────── */
    i2c_config_t i2c_cfg = {
        .mode             = I2C_MODE_MASTER,
        .sda_io_num       = I2C_SDA_GPIO,
        .scl_io_num       = I2C_SCL_GPIO,
        .sda_pullup_en    = GPIO_PULLUP_ENABLE,
        .scl_pullup_en    = GPIO_PULLUP_ENABLE,
        .master.clk_speed = 400000,
    };
    esp_err_t i2c_err = i2c_param_config(AXP_I2C_NUM, &i2c_cfg);
    if (i2c_err == ESP_OK) {
        i2c_err = i2c_driver_install(AXP_I2C_NUM, I2C_MODE_MASTER, 0, 0, 0);
    }
    if (i2c_err != ESP_OK) {
        ESP_LOGE(TAG, "I2C init failed: %s", esp_err_to_name(i2c_err));
        return i2c_err;
    }
    ESP_LOGI(TAG, "I2C ready (SDA=%d SCL=%d)", I2C_SDA_GPIO, I2C_SCL_GPIO);

    /* ── AXP192 PMU — power LCD via DCDC3 + LDO2, keep vibration off ─── */
    /*
    * Core2 reference rail mapping (AXP192):
    *   - DCDC3 (reg 0x27) -> LCD/backlight rail (set to 2.8V)
    *   - LDO2  (reg 0x28 hi nibble) -> LCD logic rail (3.3V)
    *   - LDO3  (reg 0x28 lo nibble) -> vibration motor preset (2.0V), keep disabled
    *   - Enable bits in reg 0x12: DCDC3 bit1 + LDO2 bit2 ON, LDO3 bit3 OFF
    *   - Toggle LCD reset via reg 0x96 bit1 (SetLCDRSet equivalent)
     */
    uint8_t reg30 = 0;
    if (axp_read(0x30, &reg30) == ESP_OK) {
        axp_write(0x30, (reg30 & 0x04) | 0x02); /* disable VBUS current limit */
    }
    axp_write(0x82, 0xFF);              /* enable all ADC channels              */
    axp_write(0x27, 0x54);              /* DCDC3 = 2.8V                         */
    axp_write(0x28, 0xF2);              /* LDO2 = 3.3V, LDO3 preset = 2.0V      */
    axp_read_modify_write(0x12, 0x0E, 0x06); /* ON: DCDC3+LDO2, OFF: LDO3          */
    axp_read_modify_write(0x96, 0x02, 0x00); /* LCD reset low                        */
    vTaskDelay(pdMS_TO_TICKS(20));
    axp_read_modify_write(0x96, 0x02, 0x02); /* LCD reset high                       */
    vTaskDelay(pdMS_TO_TICKS(50));
    ESP_LOGI(TAG, "AXP192 LCD rails on (DCDC3/LDO2), vibration rail off");

    /* ── DC pin (GPIO output) ────────────────────────────────────────── */
    gpio_config_t dc_cfg = {
        .pin_bit_mask = BIT64(LCD_DC),
        .mode         = GPIO_MODE_OUTPUT,
    };
    gpio_config(&dc_cfg);
    gpio_set_level(LCD_DC, 1);

    /* ── SPI bus ─────────────────────────────────────────────────────── */
    spi_bus_config_t bus = {
        .mosi_io_num   = LCD_MOSI,
        .miso_io_num   = LCD_MISO,
        .sclk_io_num   = LCD_CLK,
        .quadwp_io_num = -1,
        .quadhd_io_num = -1,
        .max_transfer_sz = (LCD_PHYS_W * 2) + 4,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(LCD_HOST, &bus, SPI_DMA_CH_AUTO));

    spi_device_interface_config_t dev = {
        .clock_speed_hz = LCD_CLK_HZ,
        .mode           = 0,
        .spics_io_num   = LCD_CS,
        .queue_size     = 1,
        .pre_cb         = ili_pre_cb,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(LCD_HOST, &dev, &s_spi));

    /* ── ILI9342C initialisation ─────────────────────────────────────── */
    vTaskDelay(pdMS_TO_TICKS(20));

    ili_cmd(0x01);                              /* SW_RESET          */
    vTaskDelay(pdMS_TO_TICKS(150));

    ili_cmd(0x11);                              /* SLEEP_OUT         */
    vTaskDelay(pdMS_TO_TICKS(120));

    /* ILI9342C extended init sequence (matches known-good Core2 panel bring-up). */
    { uint8_t d[] = {0xFF, 0x93, 0x42}; ili_cmd(0xC8); ili_data(d, sizeof(d)); } /* SETEXTC */
    { uint8_t d[] = {0x12, 0x12};       ili_cmd(0xC0); ili_data(d, sizeof(d)); } /* PWCTR1  */
    { uint8_t d[] = {0x03};             ili_cmd(0xC1); ili_data(d, sizeof(d)); } /* PWCTR2  */
    { uint8_t d[] = {0xF2};             ili_cmd(0xC5); ili_data(d, sizeof(d)); } /* VMCTR1  */
    { uint8_t d[] = {0xE0};             ili_cmd(0xB0); ili_data(d, sizeof(d)); }
    { uint8_t d[] = {0x01, 0x00, 0x00}; ili_cmd(0xF6); ili_data(d, sizeof(d)); }
    {
        uint8_t d[] = {
            0x00, 0x0C, 0x11, 0x04, 0x11,
            0x08, 0x37, 0x89, 0x4C, 0x06,
            0x0C, 0x0A, 0x2E, 0x34, 0x0F
        };
        ili_cmd(0xE0); ili_data(d, sizeof(d)); /* GMCTRP1 */
    }
    {
        uint8_t d[] = {
            0x00, 0x0B, 0x11, 0x05, 0x13,
            0x09, 0x33, 0x67, 0x48, 0x07,
            0x0E, 0x0B, 0x2E, 0x33, 0x0F
        };
        ili_cmd(0xE1); ili_data(d, sizeof(d)); /* GMCTRN1 */
    }
    { uint8_t d[] = {0x08, 0x82, 0x1D, 0x04}; ili_cmd(0xB6); ili_data(d, sizeof(d)); } /* DFUNCTR */
    ili_cmd(0x38);                              /* IDMOFF (idle off) */

    ili_cmd(0x3A); ili_data_byte(0x55);         /* COLMOD: RGB565    */
    ili_cmd(0x36); ili_data_byte(MADCTL_LANDSCAPE); /* MADCTL        */
    ili_cmd(0x21);                              /* INVERT_ON         */

    ili_cmd(0x29);                              /* DISPLAY_ON        */
    vTaskDelay(pdMS_TO_TICKS(20));

    /* Quick power-on diagnostic: proves whether GRAM writes are reaching LCD. */
    ESP_LOGI(TAG, "Core2 LCD RGB self-test (red/green/blue)");
    screen_fill(0xFF, 0x00, 0x00);
    vTaskDelay(pdMS_TO_TICKS(450));
    screen_fill(0x00, 0xFF, 0x00);
    vTaskDelay(pdMS_TO_TICKS(450));
    screen_fill(0x00, 0x00, 0xFF);
    vTaskDelay(pdMS_TO_TICKS(450));

    /* Blackout after test so first app frame is deterministic. */
    screen_off();

    ESP_LOGI(TAG, "ILI9342C ready (%d x %d @ %d MHz SPI)",
             lcd_w(), lcd_h(), LCD_CLK_HZ / 1000000);

    /* ── Touch (FT6336U) ─────────────────────────────────────────────── */
    /* I2C already init'd above; just verify the device is responsive */
    uint8_t reg_00 = 0x00, dev_mode = 0;
    esp_err_t t_err = i2c_master_write_read_device(AXP_I2C_NUM, TOUCH_I2C_ADDR,
                                                    &reg_00, 1, &dev_mode, 1,
                                                    pdMS_TO_TICKS(20));
    if (t_err == ESP_OK) {
        ESP_LOGI(TAG, "FT6336U touch ready (I2C addr=0x%02X)", TOUCH_I2C_ADDR);
        xTaskCreate(touch_poll_task, "touch_poll", 2048, NULL, 5, NULL);
    } else {
        ESP_LOGW(TAG, "FT6336U not found (err=%d) — scroll by touch unavailable", t_err);
    }

    return ESP_OK;
}

