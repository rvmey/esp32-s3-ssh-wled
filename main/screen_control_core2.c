/* Touch (FT6336U)                                                     */
/* ------------------------------------------------------------------ */

static bool touch_read_point(uint16_t *tx, uint16_t *ty)
{
    uint8_t reg  = 0x02;
    uint8_t npts = 0;
    if (i2c_master_write_read_device(AXP_I2C_NUM, TOUCH_I2C_ADDR,
                                     &reg, 1, &npts, 1,
                                     pdMS_TO_TICKS(10)) != ESP_OK) {
        return false;
    }
    if (npts == 0 || npts > 5) return false;

    uint8_t data[4];
    uint8_t data_reg = 0x03;
    if (i2c_master_write_read_device(AXP_I2C_NUM, TOUCH_I2C_ADDR,
                                     &data_reg, 1, data, 4,
                                     pdMS_TO_TICKS(10)) != ESP_OK) {
        return false;
    }
    *tx = (uint16_t)(((data[0] & 0x0F) << 8) | data[1]);
    *ty = (uint16_t)(((data[2] & 0x0F) << 8) | data[3]);
    return true;
}

static void touch_poll_task(void *arg)
{
    (void)arg;
    bool    touching     = false;
    int     start_scroll = 0;
    int16_t start_v      = 0;

    for (;;) {
        vTaskDelay(pdMS_TO_TICKS(20));

        uint16_t tx, ty;
        bool have = touch_read_point(&tx, &ty);

        /*
         * For Core2 (320×240 landscape native), the FT6336U reports:
         *   raw X (0..319) = horizontal axis of the physical panel
         *   raw Y (0..239) = vertical axis of the physical panel
         *
         * In landscape mode (320×240): vertical scroll axis = raw Y
         * In portrait mode (software-rotated 240×320): vertical scroll
         *   axis in logical space maps to raw X in physical space.
         */
        int16_t raw_v = s_landscape ? (int16_t)ty : (int16_t)tx;
        int     sign  = 1;   /* increasing raw_v = scrolling down */

        if (have && !touching) {
            touching     = true;
            start_scroll = s_scroll_line;
            start_v      = raw_v;
        } else if (have) {
            int row_h = 16 * s_font_scale;
            if (row_h == 0) continue;
            int delta_lines = sign * ((int)raw_v - (int)start_v) / row_h;
            apply_scroll_abs(start_scroll + delta_lines);
        } else if (touching) {
            touching = false;
        }
    }
}

