/* ------------------------------------------------------------------ */
/* Hardware constants (Guition JC3248W535)                            */
/* ------------------------------------------------------------------ */

#define LCD_HOST     SPI2_HOST
#define LCD_CLK_HZ   (40 * 1000 * 1000)

#define LCD_D0       21   /* MOSI / quad D0 */
#define LCD_D1       48   /* MISO / quad D1 */
#define LCD_D2       40   /* WP   / quad D2 */
#define LCD_D3       39   /* HD   / quad D3 */
#define LCD_CLK      47
#define LCD_CS       45
#define LCD_BL       1    /* backlight (active-high) */

#define LCD_PHYS_W      320   /* physical panel columns */
#define LCD_PHYS_H      480   /* physical panel rows    */
#define LCD_MAX_DIM     LCD_PHYS_H  /* longest dimension, used for text grid sizing */

/* ------------------------------------------------------------------ */
/* State                                                               */
/* ------------------------------------------------------------------ */

static spi_device_handle_t s_spi       = NULL;
static uint8_t             s_r, s_g, s_b;                  /* background colour */
static uint8_t             s_fr = 0xFF, s_fg = 0xFF, s_fb = 0xFF; /* text colour (default white) */
static bool                s_landscape = false;
static int                 s_font_scale = 2; /* screen pixels per font pixel; 1-6 */
static char                s_text[512];      /* last text drawn; empty = none */

/* Scroll state — updated by touch task and screen_draw_text */
static int               s_scroll_line  = 0;   /* first visible wrapped line   */
static int               s_scroll_total = 0;   /* total lines from last draw   */

/* Guards concurrent SPI access (touch task vs SSH/HTTP task) */
static SemaphoreHandle_t s_draw_mutex   = NULL;

/* ------------------------------------------------------------------ */
/* Touch controller (AXS15231B built-in capacitive touch, I2C)        */
/* ------------------------------------------------------------------ */

#define TOUCH_I2C_NUM  I2C_NUM_0
#define TOUCH_I2C_ADDR 0x3B
#define TOUCH_SDA_GPIO 4
#define TOUCH_SCL_GPIO 8

/* Forward declaration — defined after screen_draw_text */
static void touch_poll_task(void *arg);

/* Logical width/height — swapped in landscape mode */
static inline int lcd_w(void) { return s_landscape ? LCD_PHYS_H : LCD_PHYS_W; }
static inline int lcd_h(void) { return s_landscape ? LCD_PHYS_W : LCD_PHYS_H; }

/* One row of pixel data (RGB565) — widest row is LCD_MAX_DIM pixels */
static uint8_t s_row_buf[LCD_MAX_DIM * 2];

/* ------------------------------------------------------------------ */
/* Low-level SPI helpers                                              */
/* ------------------------------------------------------------------ */

/*
 * Send a register-write command (prefix 0x02, single-wire data).
 * The header [0x02, 0x00, cmd, 0x00] is encoded as the SPI command+address
 * phases, so the whole transaction is a single CS assertion.
 *
 * len may be 0 for commands that carry no data (SLEEP_OUT, DISPLAY_ON, …).
 *
 * The data pointer must remain valid for the duration of the call.
 */
static void axs_cmd(uint8_t cmd, const uint8_t *data, size_t len)
{
    spi_transaction_ext_t t = {
        .base = {
            .flags      = SPI_TRANS_VARIABLE_CMD | SPI_TRANS_VARIABLE_ADDR,
            .cmd        = 0x02,
            .addr       = (uint32_t)cmd << 8,   /* [0x00][cmd][0x00] */
            .length     = len * 8,
            .tx_buffer  = (len > 0) ? data : NULL,
        },
        .command_bits = 8,
        .address_bits = 24,
    };
    ESP_ERROR_CHECK(spi_device_polling_transmit(s_spi, (spi_transaction_t *)&t));
}

/*
 * Write one physical panel row (320 RGB565 pixels).
 *
 * The panel stays in its native portrait addressing mode at all times.
 * Landscape is handled in software by rotating logical coordinates before
 * they are written into these physical rows.
 */
static void axs_write_phys_row(bool is_continue)
{
    uint8_t ram_cmd = is_continue ? 0x3C : 0x2C;

    spi_transaction_ext_t t = {
        .base = {
            .flags     = SPI_TRANS_VARIABLE_CMD | SPI_TRANS_VARIABLE_ADDR
                       | SPI_TRANS_MODE_QIO,
            .cmd       = 0x32,
            .addr      = (uint32_t)ram_cmd << 8,
            .length    = (LCD_PHYS_W * 2u) * 8u,
            .tx_buffer = s_row_buf,
        },
        .command_bits = 8,
        .address_bits = 24,
    };
    ESP_ERROR_CHECK(spi_device_polling_transmit(s_spi, (spi_transaction_t *)&t));
}

/* ------------------------------------------------------------------ */
/* Internal fill                                                       */
/* ------------------------------------------------------------------ */

static void screen_fill(uint8_t r, uint8_t g, uint8_t b)
{
    xSemaphoreTake(s_draw_mutex, portMAX_DELAY);

    /* Convert to RGB565 big-endian */
    uint16_t px = ((uint16_t)(r & 0xF8) << 8)
                | ((uint16_t)(g & 0xFC) << 3)
                | (b >> 3);
    uint8_t ph = (uint8_t)(px >> 8);
    uint8_t pl = (uint8_t)(px & 0xFF);

    int w = LCD_PHYS_W;
    int h = LCD_PHYS_H;

    for (int i = 0; i < w * 2; i += 2) {
        s_row_buf[i]     = ph;
        s_row_buf[i + 1] = pl;
    }

    /* Full physical-panel address window. */
    uint8_t caset[] = { 0x00, 0x00, (uint8_t)((LCD_PHYS_W-1)>>8), (uint8_t)((LCD_PHYS_W-1)&0xFF) };
    uint8_t raset[] = { 0x00, 0x00, (uint8_t)((LCD_PHYS_H-1)>>8), (uint8_t)((LCD_PHYS_H-1)&0xFF) };
    axs_cmd(0x2A, caset, sizeof(caset));   /* CASET: 0..319 (physical columns) */
    axs_cmd(0x2B, raset, sizeof(raset));   /* RASET: 0..479 (physical rows)    */

    /* Stream rows: first uses RAMWR, remaining use RAMWRC */
    axs_write_phys_row(false);
    for (int y = 1; y < h; y++) {
        axs_write_phys_row(true);
    }

    xSemaphoreGive(s_draw_mutex);
}

