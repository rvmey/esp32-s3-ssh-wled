esp_err_t screen_init(void)
{
    s_draw_mutex = xSemaphoreCreateMutex();

    /* Backlight on */
    gpio_config_t bl = {
        .pin_bit_mask = BIT64(LCD_BL),
        .mode         = GPIO_MODE_OUTPUT,
    };
    gpio_config(&bl);
    gpio_set_level(LCD_BL, 1);

    /* SPI bus — QSPI, 40 MHz */
    spi_bus_config_t bus = {
        .mosi_io_num   = LCD_D0,
        .miso_io_num   = LCD_D1,
        .sclk_io_num   = LCD_CLK,
        .quadwp_io_num = LCD_D2,
        .quadhd_io_num = LCD_D3,
        .max_transfer_sz = (LCD_MAX_DIM * 2) + 4,   /* header + one row  */
        .flags           = SPICOMMON_BUSFLAG_QUAD,
    };
    ESP_ERROR_CHECK(spi_bus_initialize(LCD_HOST, &bus, SPI_DMA_CH_AUTO));

    /* SPI device — HALFDUPLEX required for QIO mode; CS_KEEP_ACTIVE only
     * requires spi_device_acquire_bus() which we already call, so both
     * flags can coexist. */
    spi_device_interface_config_t dev = {
        .clock_speed_hz = LCD_CLK_HZ,
        .mode           = 0,
        .spics_io_num   = LCD_CS,
        .queue_size     = 1,
        .flags          = SPI_DEVICE_HALFDUPLEX,
    };
    ESP_ERROR_CHECK(spi_bus_add_device(LCD_HOST, &dev, &s_spi));

    /* AXS15231 initialisation */
    vTaskDelay(pdMS_TO_TICKS(20));

    axs_cmd(0x01, NULL, 0);              /* SW_RESET             */
    vTaskDelay(pdMS_TO_TICKS(120));

    axs_cmd(0x11, NULL, 0);              /* SLEEP_OUT            */
    vTaskDelay(pdMS_TO_TICKS(120));

    /* Vendor unlock → set internal config → vendor lock */
    static const uint8_t bb_unlock[] = { 0x00,0x00,0x00,0x00,0x00,0x00,0x5A,0xA5 };
    static const uint8_t c1_data[]   = { 0x33 };
    static const uint8_t bb_lock[]   = { 0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00 };
    axs_cmd(0xBB, bb_unlock, sizeof(bb_unlock));
    axs_cmd(0xC1, c1_data,   sizeof(c1_data));
    axs_cmd(0xBB, bb_lock,   sizeof(bb_lock));

    static const uint8_t colmod[] = { 0x55 };   /* 16-bit RGB565 */
    static const uint8_t madctl[] = { 0x00 };   /* normal orientation */
    axs_cmd(0x3A, colmod, sizeof(colmod));       /* COLMOD  */
    axs_cmd(0x36, madctl, sizeof(madctl));       /* MADCTL  */
    axs_cmd(0x20, NULL, 0);                      /* INVERT_OFF */

    /* Write black into GRAM before enabling the display output so the
     * panel never shows uninitialised framebuffer contents (snow). */
    screen_off();

    axs_cmd(0x29, NULL, 0);                      /* DISPLAY_ON */
    vTaskDelay(pdMS_TO_TICKS(20));

    ESP_LOGI(TAG, "AXS15231 ready (%d x %d @ 40 MHz QSPI)", lcd_w(), lcd_h());

    /* Touch controller — AXS15231B built-in capacitive touch via I2C */
    i2c_config_t i2c_cfg = {
        .mode             = I2C_MODE_MASTER,
        .sda_io_num       = TOUCH_SDA_GPIO,
        .scl_io_num       = TOUCH_SCL_GPIO,
        .sda_pullup_en    = GPIO_PULLUP_ENABLE,
        .scl_pullup_en    = GPIO_PULLUP_ENABLE,
        .master.clk_speed = 400000,
    };
    esp_err_t i2c_err = i2c_param_config(TOUCH_I2C_NUM, &i2c_cfg);
    if (i2c_err == ESP_OK) {
        i2c_err = i2c_driver_install(TOUCH_I2C_NUM, I2C_MODE_MASTER, 0, 0, 0);
    }
    if (i2c_err == ESP_OK) {
        ESP_LOGI(TAG, "Touch I2C ready (SDA=%d SCL=%d addr=0x%02X)",
                 TOUCH_SDA_GPIO, TOUCH_SCL_GPIO, TOUCH_I2C_ADDR);
        xTaskCreate(touch_poll_task, "touch_poll", 2048, NULL, 5, NULL);
    } else {
        ESP_LOGW(TAG, "Touch I2C init failed (%d) — scroll by touch unavailable", i2c_err);
    }

    return ESP_OK;
}

void screen_set_color(uint8_t r, uint8_t g, uint8_t b)
{
    s_r = r;  s_g = g;  s_b = b;
    if (s_text[0]) {
        screen_draw_text(s_text);
    } else {
        screen_fill(r, g, b);
    }
}

void screen_off(void)
{
    s_r = s_g = s_b = 0;
    s_text[0] = '\0';
    screen_fill(0, 0, 0);
}

void screen_get_color(uint8_t *r, uint8_t *g, uint8_t *b)
{
    *r = s_r;  *g = s_g;  *b = s_b;
}

void screen_set_landscape(bool landscape)
{
    s_landscape = landscape;
    /* Keep the controller in native portrait mode and rotate in software. */
    if (s_text[0]) {
        screen_draw_text(s_text);
    } else {
        screen_fill(s_r, s_g, s_b);
    }
}

void screen_get_landscape(bool *landscape)
{
    *landscape = s_landscape;
}

void screen_set_font_scale(int scale)
{
    if (scale < 1) scale = 1;
    if (scale > 8) scale = 8;
    s_font_scale = scale;
    if (s_text[0]) {
        screen_draw_text(s_text);
    }
}

void screen_get_font_scale(int *scale)
{
    *scale = s_font_scale;
}

void screen_set_text_color(uint8_t r, uint8_t g, uint8_t b)
{
    s_fr = r;  s_fg = g;  s_fb = b;
    if (s_text[0]) {
        screen_draw_text(s_text);
    }
}

void screen_get_text_color(uint8_t *r, uint8_t *g, uint8_t *b)
{
    *r = s_fr;  *g = s_fg;  *b = s_fb;
}

