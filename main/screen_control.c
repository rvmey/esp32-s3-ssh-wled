/* Read one touch sample from the AXS15231B touch controller.
 * Protocol: write the 8-byte read-touchpad command (separate transaction),
 * then read 8 bytes (separate transaction — STOP between write and read).
 * Returns true (and sets *x / *y) when at least one finger is active.
 * Raw coordinates: x = 0..319 (columns), y = 0..479 (rows) in portrait. */
static bool touch_read_point(uint16_t *x, uint16_t *y)
{
    static const uint8_t cmd[8] = {
        0xB5, 0xAB, 0xA5, 0x5A, 0x00, 0x00, 0x00, 0x08
    };
    uint8_t data[8] = {0};
    /* Two separate transactions (STOP between write and read) */
    esp_err_t err = i2c_master_write_to_device(
        TOUCH_I2C_NUM, TOUCH_I2C_ADDR,
        cmd, sizeof(cmd),
        pdMS_TO_TICKS(10));
    if (err != ESP_OK) return false;
    err = i2c_master_read_from_device(
        TOUCH_I2C_NUM, TOUCH_I2C_ADDR,
        data, sizeof(data),
        pdMS_TO_TICKS(10));
    if (err != ESP_OK) return false;
    if (data[0] != 0 || data[1] == 0) return false;  /* no active touch */
    *x = ((uint16_t)(data[2] & 0x0F) << 8) | data[3];
    *y = ((uint16_t)(data[4] & 0x0F) << 8) | data[5];
    return true;
}

/* Apply a touch-driven scroll to an absolute line position and redraw.
 * Called by the touch task while a finger is down.                       */
static void apply_scroll_abs(int new_scroll)
{
    if (!s_text[0]) return;

    int row_h      = 16 * s_font_scale;
    int max_rows   = lcd_h() / row_h;
    int max_scroll = s_scroll_total - max_rows;
    if (max_scroll <= 0) return;

    if (new_scroll < 0)          new_scroll = 0;
    if (new_scroll > max_scroll) new_scroll = max_scroll;

    if (new_scroll != s_scroll_line) {
        s_scroll_line = new_scroll;
        screen_draw_text(s_text);
    }
}

/* Touch polling task.
 * Polls the AXS15231B touch controller every 20 ms and live-scrolls the
 * text while the finger is moving.
 *
 * Axis mapping (from the chip mounting on the JC3248W535 board):
 *   Portrait:  raw X (data[2..3]) = display vertical axis (due to swap_xy
 *              + mirror_y transform in the chip mount).  Swipe up →
 *              raw-X increases → s_scroll_line increases (show content below).
 *   Landscape: raw Y (data[4..5]) = display vertical axis.  Swipe up →
 *              raw-Y decreases → negate to get the same sign convention.    */
static void touch_poll_task(void *arg)
{
    (void)arg;
    bool    touching     = false;
    int     start_scroll = 0;
    int16_t start_v      = 0;   /* raw vertical-axis sample at touch-down */

    for (;;) {
        vTaskDelay(pdMS_TO_TICKS(20));

        uint16_t tx, ty;
        bool have = touch_read_point(&tx, &ty);

        /* Select the raw axis that maps to the display's vertical direction.
         * The AXS15231B on JC3248W535: chip raw-Y = screen vertical (portrait),
         *    chip raw-X = screen vertical (landscape).
         * sign=-1: swipe down decreases raw_v → positive delta → scroll down. */
        int16_t raw_v = s_landscape ? (int16_t)tx : (int16_t)ty;
        int     sign  = -1;

        if (have && !touching) {
            /* Finger just touched down */
            touching     = true;
            start_scroll = s_scroll_line;
            start_v      = raw_v;
        } else if (have) {
            /* Finger still down — live-scroll as it moves */
            int row_h = 16 * s_font_scale;
            if (row_h == 0) continue;
            int delta_lines = sign * ((int)raw_v - (int)start_v) / row_h;
            apply_scroll_abs(start_scroll + delta_lines);
        } else if (touching) {
            /* Finger lifted */
            touching = false;
        }
    }
}
