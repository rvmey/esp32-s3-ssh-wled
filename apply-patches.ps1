# ---------------------------------------------------------------------------
# Patch 0005 - spi_bus_lock.c: fix TOCTOU null-deref in bg_exit_core
# ESP-IDF v6.0 bug: lock->acquiring_dev is volatile and can be set to NULL by
# another task between the null-check (line 548) and DEV_BG_MASK() dereference
# (line 549), causing LoadProhibited at EXCVADDR=0x8 (NULL+8).
# Fix: snapshot the pointer once into a local variable before the check.
# ---------------------------------------------------------------------------
$idfSpiLock = "C:\esp\v6.0\esp-idf\components\esp_hw_support\spi_bus_lock.c"
if (-not (Test-Path $idfSpiLock)) {
    Write-Warning "SKIP  0005 spi_bus_lock.c TOCTOU fix  (ESP-IDF not found at C:\esp\v6.0)"
} else {
    # Use single-quoted here-strings so backtick characters in C comments are
    # treated literally (double-quoted @"..."@ would consume them as PS escapes).
    $old05 = @'
    bool ret;
    uint32_t status = lock_status_fetch(lock);
    if (lock->acquiring_dev) {
        if (status & DEV_BG_MASK(lock->acquiring_dev)) {
            BUS_LOCK_DEBUG_EXECUTE_CHECK(lock->acq_dev_bg_active);
            ret = false;
        } else {
            // The request may happen any time, even after we fetched the status.
            // The value of `acq_dev_bg_active` is random.
            resume_dev_in_isr(lock->acquiring_dev, do_yield);
            ret = true;
        }
    } else {
'@
    $new05 = @'
    bool ret;
    uint32_t status = lock_status_fetch(lock);
    /* Snapshot acquiring_dev once: it is a volatile pointer and can be set to
     * NULL by another task between the null-check and the dereference, causing
     * a LoadProhibited crash (EXCVADDR=0x8) in DEV_BG_MASK(). */
    spi_bus_lock_dev_t *acq_dev = lock->acquiring_dev;
    if (acq_dev) {
        if (status & DEV_BG_MASK(acq_dev)) {
            BUS_LOCK_DEBUG_EXECUTE_CHECK(lock->acq_dev_bg_active);
            ret = false;
        } else {
            // The request may happen any time, even after we fetched the status.
            // The value of `acq_dev_bg_active` is random.
            resume_dev_in_isr(acq_dev, do_yield);
            ret = true;
        }
    } else {
'@
    # Normalise CRLF->LF in case the PS script has CRLF line endings
    $old05n = $old05 -replace "`r`n", "`n"
    $new05n = $new05 -replace "`r`n", "`n"
    $content = Get-Content $idfSpiLock -Raw -Encoding UTF8
    if ($content.Contains($new05n)) {
        Write-Host "SKIP  0005 spi_bus_lock.c TOCTOU fix  (already applied)" -ForegroundColor DarkGray
    } elseif (-not $content.Contains($old05n)) {
        Write-Warning "SKIP  0005 spi_bus_lock.c TOCTOU fix  (original text not found - ESP-IDF version may differ)"
    } else {
        $patched = $content.Replace($old05n, $new05n)
        [System.IO.File]::WriteAllText($idfSpiLock, $patched, [System.Text.UTF8Encoding]::new($false))
        Write-Host "OK    0005 spi_bus_lock.c TOCTOU fix" -ForegroundColor Green
    }
}

