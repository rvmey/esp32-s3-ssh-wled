<#
.SYNOPSIS
    Apply patches to managed_components/ and ESP-IDF that fix known issues.

.DESCRIPTION
    managed_components/ is regenerated by IDF Component Manager on every clean
    build, so these fixes cannot be committed to git.  Run this script once
    after a fresh clone (or after wiping managed_components/) to restore the
    working state.

    Patches applied:
      0001  esp_sdk_mem_lib.c  - rename enum member thread_local to
                                 thread_local_seg (C23 made it a keyword)
      0002  user_settings.h    - disable wolfssl HW acceleration for ESP32-S3
                                 (PERIPH_AES_MODULE / PERIPH_RSA_MODULE removed
                                 from ESP-IDF v6.0 periph_defs.h)
      0003  user_settings.h    - add WOLFSSL_ED25519_STREAMING_VERIFY
      0004  user_settings.h    - disable wolfssl HW acceleration for classic ESP32
                                 (same IDF v6.0 PERIPH_RSA_MODULE / clk_gate_ll breakage)
      0005  spi_bus_lock.c     - fix TOCTOU null-deref in bg_exit_core (ESP-IDF v6.0 bug)
                                 acquiring_dev is volatile and can be cleared between the
                                 null-check and the DEV_BG_MASK() dereference; snapshot
                                 it into a local variable to close the race window.
                                 Crash symptom: LoadProhibited at EXCVADDR=0x8 inside
                                 ppTask -> bg_exit_core (spi_bus_lock.c:549) during
                                 BT A2DP + WiFi operation.
#>
Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$Root = $PSScriptRoot

function Patch-File {
    param(
        [string]$RelPath,
        [string]$OldText,
        [string]$NewText,
        [string]$Description
    )

    $FullPath = Join-Path $Root $RelPath

    if (-not (Test-Path $FullPath)) {
        Write-Warning "SKIP  $RelPath  (file not found - run idf.py build once first to populate managed_components/)"
        return
    }

    $content = Get-Content $FullPath -Raw -Encoding UTF8

    if ($content.Contains($NewText)) {
        Write-Host "SKIP  $Description  (already applied)" -ForegroundColor DarkGray
        return
    }

    if (-not $content.Contains($OldText)) {
        Write-Warning "SKIP  $Description  (original text not found - wolfssl version may differ)"
        return
    }

    $patched = $content.Replace($OldText, $NewText)
    [System.IO.File]::WriteAllText($FullPath, $patched, [System.Text.UTF8Encoding]::new($false))
    Write-Host "OK    $Description" -ForegroundColor Green
}

